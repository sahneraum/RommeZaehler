import java.util.Scanner;

public class Main {

    public static void main(String[] args) {
        new Main();
    }

    public Main() {
        // totalScore will be set to 0
        int totalScore = 0;

        //actualScore will be set to the input score
        Scanner scanner = new Scanner();

        int actualScore = scanner;

        // The actualScore will be added to the totalScore
        totalScore = totalScore + actualScore;

        // print the totalScore
        System.out.println(totalScore);
    }

}